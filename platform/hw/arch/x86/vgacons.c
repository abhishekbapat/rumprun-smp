static void
cons_putat(int c, int x, int y)
{
	cons_buf[x + y*cons_width] = CONS_MAGENTA|c;
}

/* display a character in the next available slot */
void
vgacons_putc(int c)
{
	static int cons_x;
	static int cons_y;
	int x;
	int doclear = 0;

	if (c == '\n') {
		cons_x = 0;
		cons_y++;
		doclear = 1;
	} else if (c == '\r') {
		cons_x = 0;
	} else if (c == '\t') {
		cons_x = (cons_x+8) & ~7;
	} else {
		cons_putat(c, cons_x++, cons_y);
	}
	if (cons_x == cons_width) {
		cons_x = 0;
		cons_y++;
		doclear = 1;
	}
	if (cons_y == cons_height) {
		cons_y--;
		/* scroll screen up one line */
		for (x = 0; x < (cons_height-1)*cons_width; x++)
			cons_buf[x] = cons_buf[x+cons_width];
	}
	if (doclear) {
		for (x = 0; x < cons_width; x++)
			cons_putat(' ', x, cons_y);
	}
}

static void fb_init(unsigned int *fb, unsigned int width, unsigned int height)
{
	uint64_t i, num = (uint64_t) width * height;

	/* Clean up the screen */
	for (i = 0; i < num; i++) {
		fb[i] = 0x00000000U;
	}

  Fb = fb;
	PosX = 0;
	PosY = 0;
	MaxX = width / FONT_WIDTH;
	MaxY = height / FONT_HEIGHT;
}

void vgacons_init(unsigned int *fb_addr, unsigned int fb_height, unsigned int fb_width)
{
	cons_buf = (volatile uint16_t *)fb_addr;
	cons_height = fb_height;
	cons_width = fb_width;

	if(((uint64_t)(fb_addr)) != 0xb8000) // Means UEFI
	{
		fb_init(fb_addr, fb_width, fb_height);
	}
}

static void fb_scrollup(void)
{
	/* Move the text up one row */
	uint64_t cur = 0, count = cons_width * ((MaxY - 1) * FONT_HEIGHT);
	uint64_t row = cons_width * FONT_HEIGHT;
	do {
		Fb[cur] = Fb[cur+row];
		cur++;
	} while (--count != 0);

	/* Clean up the last row */
	do {
		Fb[cur] = 0x00000000U;
		cur++;
	} while (--row != 0);
}

void fb_putc(int ch)
{
  uint64_t cur;
	unsigned char *ptr;
	if ((signed char) ch <= 0) { /* not in the ASCII subset */
		if (ch == 0) return;
		ch = '?'; /* an unknown character */
	}
	if (ch == '\n' || PosX == MaxX) {
		PosX = 0;
		PosY++;
	}
	if (PosY == MaxY) {
		PosY--;
		fb_scrollup();
	}
	if (ch == '\n')
		return;
	ptr = &__ascii_font[(unsigned char) ch * (FONT_WIDTH * FONT_HEIGHT / 8)];
	cur = (uint64_t) PosX * FONT_WIDTH + (PosY * FONT_HEIGHT) * cons_width;
	for (uint64_t j = 0; j < FONT_HEIGHT; j++) {
		/* for simplicity, assume that FONT_WIDTH=8, i.e., fits in one byte */
		signed char bitmap = ptr[j];
		for (uint64_t i = 0; i < FONT_WIDTH; i++) {
			signed char color = (bitmap >> 7); /* propagate the sign bit */
			Fb[cur + i] = (signed int) color; /* sign extend to 32 bits */
			bitmap <<= 1;
		}
		cur += cons_width;
	}
	PosX++;
}